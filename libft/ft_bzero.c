#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	ctr;
	char	*ptr;

	ctr = 0;
	ptr = s;
	while (ctr < n)
	{
		ptr[ctr] = '\0';
		ctr++;
	}
}
